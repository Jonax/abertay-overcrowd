#ifndef _WIN_32_H_
#define _WIN_32_H_

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//	LIBRARY INCLUDES
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#include <windows.h>			// Win32 library.  
#include "Renderer/GfxDX9.h"	// Renderer module for DirectX 9.

class Win32
{
	public:
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//	CLASS MEMBERS
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		Win32();				// Class constructor.  
		~Win32();				// Class destructor.  

		bool Register(HINSTANCE hInstance, WNDPROC WndProc);			// Registers a Win32 class.  
		bool Create(HINSTANCE hInstance);								// Creates a Win32 window.  
		void Unregister();												// Unregisters the attached Win32 class.  

		bool Render();													// Main rendering loop.  
		void MsgProcess(UINT message, WPARAM wParam, LPARAM lParam);	// Main Win32 message handler.  

		void Show();													// Shows the created Win32 window.  
		void AttachRenderer();											// Attaches the relevant renderer to the window.  

		int GetReturn();												// Generates a return code for shutdown.  

	private:
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//	CLASS OBJECTS
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		GfxDX9*		Renderer;			// Pointer for a renderer module.  

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//	CLASS DATA
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		HWND		hwnd;				// Handle to the window.
		WNDCLASSEX	windowClass;		// Window class object.
		MSG			msg;				// Message object.

		float		secsPerCnt;
		__int64		prevTimeStamp;
};

#endif